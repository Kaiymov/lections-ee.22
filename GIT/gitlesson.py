# Команды  Git

# 1. git init - она создает новый гит репозитори локальной на ваше пк

# 2. git commit - как только мы достигаем определенного момента в разработке,
# то когда мы сохраняем и комментируем изменения.
# (фиксация измнения в репо)
# git commit -m '<comment>'

# 3. git add - когда мы создаем или измняем файлы,
# то при помощи этой команды мы закружаем все изменения в локальной хранилизеще

# git add <названия файла>
# git add .